package lesson4;

/**
 * Напишіть програму для обчислення суми перших 10 натуральних чисел.
 */
public class TaskSeven {
    public static void main(String[] args) {
        int n = 10;
        int sum = n * (n + 1) / 2;
        System.out.println("Сума перших 10 натуральних чисел: " + sum);
    }
}
