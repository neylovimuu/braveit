package lesson3;

public class TaskOne {
    public static void main(String[] args){
        double y = 4.0;
        double w = 6.0;
        System.out.println(y+w);
        System.out.println(y*w);
        System.out.println(y-w);
        System.out.println(y/w);
        System.out.println(y%w);
        System.out.println(++y%w);
    }
}
